#include <unistd.h>

int	main(int ac, char *av[])
{
	int i = -1;
	int j = -1;
	int used[127];

	if (ac == 3)
	{
		while (++i < 127)
			used[i] = 0;
		i = -1;
		while (av[1][++i])
		{
			j = -1;
			while (av[2][++j])
			{
				if (av[1][i] == av[2][j] && used[(int)av[1][i]] == 0)
				{
					write (1, &av[1][i], 1);
					used[(int)av[1][i]] = 1;
				}
			}
		}	
	}
	write (1, "\n", 1);
}